package items;

public class TimeToken {
	private boolean isFlipped = false;
	private final int hourGlass = 1;

	// Getters and Setters
	public boolean isFlipped() {
		return isFlipped;
	}

	public void setFlipped(boolean isFlipped) {
		this.isFlipped = isFlipped;
	}

	public int getHourGlass() {
		return hourGlass;
	}
}
